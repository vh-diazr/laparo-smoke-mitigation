"""
smoke_simulation.py
===================
Physics-based 2D surgical smoke simulation using the Stable Fluids framework.

Scientific context
------------------
Surgical smoke generated by electrocautery instruments exhibits spatially
nonhomogeneous, temporally dynamic density distributions within the
insufflated abdominal cavity. This module implements a 2D numerical solver
for the incompressible Navier-Stokes equations to model the fluid dynamics
of surgical smoke, producing physically consistent smoke flux fields for
use in synthetic dataset construction.

The solver follows the Stable Fluids formulation (Stam, 1999) and includes:
  - Semi-Lagrangian advection for unconditional numerical stability
  - Implicit viscous diffusion via Gauss-Seidel relaxation
  - Pressure projection to enforce the divergence-free velocity constraint
  - Vorticity confinement to preserve small-scale rotational flow structures
  - Thermal buoyancy to model smoke rise behaviour
  - Combustion and fuel dissipation dynamics

Output
------
The simulation produces a density frame sequence dens_frames of shape
(steps, N, N), representing the spatially varying smoke flux field
rho(x, y, t) at each simulation timestep. This array is saved as
smoke_flux.npy and consumed by apply_smoke_degradation.py to construct
the synthetic degraded laparoscopic image dataset.

Pipeline position
-----------------
  smoke_simulation.py  →  smoke_flux.npy  →  apply_smoke_degradation.py

Prerequisites
-------------
  pip install numpy imageio

Reference
---------
  Diaz-Ramirez et al., "Nonhomogeneous smoke mitigation in laparoscopic
  images using a hybrid physical-neural model", Medical & Biological
  Engineering & Computing, Springer Nature, 2025. (Under review.)

  Stam, J. (1999). Stable fluids. In Proceedings of SIGGRAPH 1999,
  ACM Press/Addison-Wesley, pp. 121-128.

Usage
-----
  python smoke_simulation.py --N 128 --steps 120 --dt 0.15 \
                             --output smoke_flux --fps 24
"""

import argparse
import math
import numpy as np


# =============================================================================
# 1. Grid allocation
# =============================================================================

def alloc(N: int) -> np.ndarray:
    """
    Allocate an (N+2) x (N+2) grid with ghost cells for boundary conditions.

    Parameters
    ----------
    N : int
        Interior grid resolution. Total allocated size is (N+2) x (N+2).

    Returns
    -------
    np.ndarray, shape (N+2, N+2), dtype float32
        Zero-initialized grid.
    """
    return np.zeros((N + 2, N + 2), dtype=np.float32)


# =============================================================================
# 2. Boundary conditions
# =============================================================================

def set_bnd(b: int, x: np.ndarray, N: int) -> None:
    """
    Enforce boundary conditions on a scalar or vector field.

    Parameters
    ----------
    b : int
        Field type selector:
          0 — scalar field (density, temperature, fuel): Neumann (copy)
          1 — x-velocity (u): no-slip on left/right walls, copy on top/bottom
          2 — y-velocity (v): no-slip on top/bottom walls, copy on left/right
    x : np.ndarray, shape (N+2, N+2)
        Field array modified in-place.
    N : int
        Interior grid resolution.
    """
    if b == 1:
        x[1:N+1, 0]   = -x[1:N+1, 1]
        x[1:N+1, N+1] = -x[1:N+1, N]
        x[0,   1:N+1] =  x[1,   1:N+1]
        x[N+1, 1:N+1] =  x[N,   1:N+1]
    elif b == 2:
        x[1:N+1, 0]   =  x[1:N+1, 1]
        x[1:N+1, N+1] =  x[1:N+1, N]
        x[0,   1:N+1] = -x[1,   1:N+1]
        x[N+1, 1:N+1] = -x[N,   1:N+1]
    else:
        x[1:N+1, 0]   = x[1:N+1, 1]
        x[1:N+1, N+1] = x[1:N+1, N]
        x[0,   1:N+1] = x[1,   1:N+1]
        x[N+1, 1:N+1] = x[N,   1:N+1]

    # Corner averaging
    x[0,    0]   = 0.5 * (x[1, 0]   + x[0, 1])
    x[0,    N+1] = 0.5 * (x[1, N+1] + x[0, N])
    x[N+1,  0]   = 0.5 * (x[N, 0]   + x[N+1, 1])
    x[N+1,  N+1] = 0.5 * (x[N, N+1] + x[N+1, N])


# =============================================================================
# 3. Linear solver
# =============================================================================

def lin_solve(b: int, x: np.ndarray, x0: np.ndarray,
              a: float, c: float, N: int, iters: int = 20) -> None:
    """
    Gauss-Seidel iterative relaxation solver for the linear system
    arising from implicit diffusion and pressure projection.

    Parameters
    ----------
    b     : int    — boundary condition selector (passed to set_bnd)
    x     : ndarray — solution field, modified in-place
    x0    : ndarray — right-hand side field
    a     : float  — off-diagonal coefficient
    c     : float  — diagonal coefficient
    N     : int    — interior grid resolution
    iters : int    — number of relaxation iterations (default: 20)
    """
    for _ in range(iters):
        x[1:N+1, 1:N+1] = (x0[1:N+1, 1:N+1] + a * (
            x[1:N+1, 0:N]   + x[1:N+1, 2:N+2] +
            x[0:N,   1:N+1] + x[2:N+2, 1:N+1]
        )) / c
        set_bnd(b, x, N)


# =============================================================================
# 4. Diffusion
# =============================================================================

def diffuse(b: int, x: np.ndarray, x0: np.ndarray,
            diff: float, dt: float, N: int, iters: int = 20) -> None:
    """
    Implicit viscous diffusion of field x with source x0.

    Solves: (I - diff * dt * N^2 * Laplacian) x = x0
    via Gauss-Seidel relaxation, ensuring unconditional stability.

    Parameters
    ----------
    b    : int    — boundary condition selector
    x    : ndarray — output field, modified in-place
    x0   : ndarray — input (source) field
    diff : float  — diffusion coefficient
    dt   : float  — timestep
    N    : int    — interior grid resolution
    iters: int    — solver iterations (default: 20)
    """
    if diff == 0.0:
        x[1:N+1, 1:N+1] = x0[1:N+1, 1:N+1]
        set_bnd(b, x, N)
        return
    a = dt * diff * N * N
    lin_solve(b, x, x0, a, 1 + 4 * a, N, iters)


# =============================================================================
# 5. Advection
# =============================================================================

def advect(b: int, d: np.ndarray, d0: np.ndarray,
           u: np.ndarray, v: np.ndarray,
           dt: float, N: int) -> None:
    """
    Semi-Lagrangian advection of scalar or vector field d.

    For each grid cell (i,j), traces the characteristic backwards in time
    by dt to find the source location, then bilinearly interpolates d0
    at that location. This formulation is unconditionally stable for any
    timestep size.

    Coordinate convention:
      j — column index (x direction, rightward)
      i — row index    (y direction, downward in memory, upward physically)
      u — x-velocity (along j)
      v — y-velocity (along i, positive upward after buoyancy sign convention)

    Parameters
    ----------
    b  : int    — boundary condition selector
    d  : ndarray — output advected field, modified in-place
    d0 : ndarray — input field at previous timestep
    u  : ndarray — x-velocity field
    v  : ndarray — y-velocity field
    dt : float  — timestep
    N  : int    — interior grid resolution
    """
    dt0 = dt * N

    ii = np.arange(1, N+1, dtype=np.float32)[:, None]   # row indices
    jj = np.arange(1, N+1, dtype=np.float32)[None, :]   # col indices

    uij = u[1:N+1, 1:N+1]
    vij = v[1:N+1, 1:N+1]

    # Back-trace source positions
    x = np.clip(jj - dt0 * uij, 0.5, N + 0.5)
    y = np.clip(ii - dt0 * vij, 0.5, N + 0.5)

    j0 = np.floor(x).astype(np.int32);  j1 = j0 + 1
    i0 = np.floor(y).astype(np.int32);  i1 = i0 + 1

    s1 = x - j0;  s0 = 1.0 - s1
    t1 = y - i0;  t0 = 1.0 - t1

    # Bilinear interpolation
    d[1:N+1, 1:N+1] = (
        s0 * (t0 * d0[i0, j0] + t1 * d0[i1, j0]) +
        s1 * (t0 * d0[i0, j1] + t1 * d0[i1, j1])
    )
    set_bnd(b, d, N)


# =============================================================================
# 6. Pressure projection
# =============================================================================

def project(u: np.ndarray, v: np.ndarray,
            p: np.ndarray, div: np.ndarray,
            N: int, iters: int = 20) -> None:
    """
    Project velocity field (u, v) onto the space of divergence-free fields.

    Solves the pressure Poisson equation:
      Laplacian(p) = divergence(u, v)
    then subtracts the pressure gradient from (u, v) to enforce
    the incompressibility constraint: div(u, v) = 0.

    Parameters
    ----------
    u, v : ndarray — velocity fields, modified in-place
    p    : ndarray — pressure field (scratch buffer)
    div  : ndarray — divergence field (scratch buffer)
    N    : int    — interior grid resolution
    iters: int    — solver iterations (default: 20)
    """
    h = 1.0 / N
    div[1:N+1, 1:N+1] = -0.5 * h * (
        u[1:N+1, 2:N+2] - u[1:N+1, 0:N] +
        v[2:N+2, 1:N+1] - v[0:N,   1:N+1]
    )
    p.fill(0.0)
    set_bnd(0, div, N)
    set_bnd(0, p,   N)

    lin_solve(0, p, div, 1.0, 4.0, N, iters)

    u[1:N+1, 1:N+1] -= 0.5 * (p[1:N+1, 2:N+2] - p[1:N+1, 0:N])   / h
    v[1:N+1, 1:N+1] -= 0.5 * (p[2:N+2, 1:N+1] - p[0:N,   1:N+1]) / h
    set_bnd(1, u, N)
    set_bnd(2, v, N)


# =============================================================================
# 7. Vorticity confinement
# =============================================================================

def compute_curl(u: np.ndarray, v: np.ndarray, N: int) -> np.ndarray:
    """
    Compute scalar vorticity omega = dv/dx - du/dy on interior cells.

    Parameters
    ----------
    u, v : ndarray — velocity fields
    N    : int    — interior grid resolution

    Returns
    -------
    np.ndarray, shape (N+2, N+2)
        Vorticity field; only interior cells [1:N+1, 1:N+1] are valid.
    """
    w = np.zeros_like(u)
    w[1:N+1, 1:N+1] = 0.5 * (
        (v[1:N+1, 2:N+2] - v[1:N+1, 0:N]) -
        (u[2:N+2, 1:N+1] - u[0:N,   1:N+1])
    )
    return w


def vorticity_confinement(u: np.ndarray, v: np.ndarray,
                          conf: float, dt: float, N: int) -> None:
    """
    Apply vorticity confinement to counteract numerical dissipation of
    small-scale rotational flow structures.

    Computes the normalized vorticity gradient N = grad(|omega|) / |grad(|omega|)|,
    then applies the confinement force F = epsilon * (N x omega * z_hat)
    to the velocity field.

    Parameters
    ----------
    u, v : ndarray — velocity fields, modified in-place
    conf : float  — confinement coefficient epsilon (0.0 = disabled)
    dt   : float  — timestep
    N    : int    — interior grid resolution
    """
    if conf == 0.0:
        return

    w     = compute_curl(u, v, N)
    abs_w = np.abs(w)

    Nx = np.zeros_like(u)
    Ny = np.zeros_like(v)

    Nx[1:N+1, 1:N+1] = 0.5 * (abs_w[1:N+1, 2:N+2] - abs_w[1:N+1, 0:N])
    Ny[1:N+1, 1:N+1] = 0.5 * (abs_w[2:N+2, 1:N+1] - abs_w[0:N,   1:N+1])

    mag = np.sqrt(Nx**2 + Ny**2) + 1e-6
    Nx /= mag
    Ny /= mag

    u[1:N+1, 1:N+1] += dt * conf *  Ny[1:N+1, 1:N+1] * w[1:N+1, 1:N+1]
    v[1:N+1, 1:N+1] += dt * conf * (-Nx[1:N+1, 1:N+1]) * w[1:N+1, 1:N+1]
    set_bnd(1, u, N)
    set_bnd(2, v, N)


# =============================================================================
# 8. Thermal buoyancy
# =============================================================================

def add_buoyancy(v: np.ndarray, temp: np.ndarray,
                 buoyancy: float, dt: float, N: int) -> None:
    """
    Apply thermal buoyancy to the vertical velocity field.

    Models smoke rise due to thermal expansion:
      v' = v - buoyancy * T * dt
    The negative sign reflects the convention that +v is downward in
    array row indexing, so buoyancy acts upward (negative v).

    Parameters
    ----------
    v        : ndarray — vertical velocity field, modified in-place
    temp     : ndarray — temperature field, normalized to [0, 1]
    buoyancy : float  — buoyancy coefficient (0.0 = disabled)
    dt       : float  — timestep
    N        : int    — interior grid resolution
    """
    if buoyancy == 0.0:
        return
    v[1:N+1, 1:N+1] -= buoyancy * temp[1:N+1, 1:N+1] * dt
    set_bnd(2, v, N)


# =============================================================================
# 9. Combustion and noise dynamics
# =============================================================================

def combustion_step(temp: np.ndarray, fuel: np.ndarray, noise: np.ndarray,
                    dt: float,
                    burn_temperature: float = 1700.0,
                    cooling: float = 3000.0,
                    noise_blending: float = 0.5,
                    fuel_dissipation: float = 0.92,
                    N: int = 64) -> None:
    """
    Advance combustion dynamics by one timestep.

    Models fuel ignition, temperature evolution under radiative cooling
    (Stefan-Boltzmann T^4 law), and fuel dissipation. Noise blending
    introduces stochastic perturbations to the effective fuel efficiency,
    producing realistic irregular smoke density variations.

    Parameters
    ----------
    temp             : ndarray — temperature field [0,1], modified in-place
    fuel             : ndarray — fuel density field [0,1], modified in-place
    noise            : ndarray — stochastic noise field [0,1]
    dt               : float  — timestep
    burn_temperature : float  — peak combustion temperature (unused scalar,
                                retained for interface compatibility)
    cooling          : float  — radiative cooling rate coefficient
    noise_blending   : float  — noise influence on fuel efficiency [0,1]
    fuel_dissipation : float  — per-frame fuel decay factor [0,1]
    N                : int    — interior grid resolution
    """
    f = np.clip(fuel, 0.0, 1.0)
    t = np.clip(temp, 0.0, 1.0)
    n = np.clip(noise, 0.0, 1.0)

    fuel_eff = f * (1.0 - 0.3 * noise_blending + 0.3 * n * noise_blending)
    t_new    = np.maximum(t, fuel_eff)

    sigma = 1.0 / max(cooling, 1.0) * 8.0
    t_new = np.clip(t_new - sigma * (t_new ** 4) * dt, 0.0, 1.0)
    f_new = np.clip(f * (fuel_dissipation ** (dt / 0.016)), 0.0, 1.0)

    temp[1:N+1, 1:N+1] = t_new[1:N+1, 1:N+1]
    fuel[1:N+1, 1:N+1] = f_new[1:N+1, 1:N+1]


def update_noise(noise: np.ndarray, dt: float,
                 volatility: float, N: int) -> None:
    """
    Update the stochastic noise field by adding uniform random perturbations.

    Parameters
    ----------
    noise      : ndarray — noise field [0,1], modified in-place
    dt         : float  — timestep
    volatility : float  — perturbation magnitude per unit time (0.0 = disabled)
    N          : int    — interior grid resolution
    """
    if volatility <= 0.0:
        return
    rand = np.random.uniform(-1.0, 1.0, size=noise.shape).astype(np.float32)
    noise[1:N+1, 1:N+1] = np.clip(
        noise[1:N+1, 1:N+1] + volatility * dt * rand[1:N+1, 1:N+1],
        0.0, 1.0
    )


