# 10. Top-level simulation
# =============================================================================

# Default fluid dynamics configuration used in the manuscript experiments.
DEFAULT_CONFIG = {
    "BUOYANCY":            0.2,
    "BURN_TEMPERATURE":    1700.0,
    "CONFINEMENT":         5.0,
    "COOLING":             1000.0,
    "DENSITY_DISSIPATION": 0.999,
    "FUEL_DISSIPATION":    0.92,
    "NOISE_BLENDING":      0.5,
    "NOISE_VOLATILITY":    0.1,
    "PRESSURE_DISSIPATION":0.8,
    "VELOCITY_DISSIPATION":0.98,
}


def simulate(
    N        : int             = 128,
    steps    : int             = 120,
    dt       : float           = 0.15,
    config   : dict | None     = None,
    emissions: list[dict]| None= None,
) -> np.ndarray:
    """
    Run the 2D Navier-Stokes surgical smoke simulation.

    Produces a temporally dynamic, spatially nonhomogeneous smoke density
    field rho(x, y, t) by advancing the fluid state for `steps` timesteps.
    Each emission event injects density, fuel, temperature, and impulse
    into a circular region of the domain, modelling the spatial onset of
    electrocautery-generated surgical smoke associated with instrument
    contact points.

    Parameters
    ----------
    N         : int
        Interior grid resolution (N x N). Default: 128.
    steps     : int
        Number of simulation timesteps. Default: 120.
    dt        : float
        Simulation timestep. Default: 0.15.
    config    : dict or None
        Fluid dynamics parameter dictionary. If None, DEFAULT_CONFIG is used.
        Keys: BUOYANCY, BURN_TEMPERATURE, CONFINEMENT, COOLING,
              DENSITY_DISSIPATION, FUEL_DISSIPATION, NOISE_BLENDING,
              NOISE_VOLATILITY, PRESSURE_DISSIPATION, VELOCITY_DISSIPATION.
    emissions : list of dict or None
        List of smoke emission event specifications. Each entry is a dict
        with the following keys:
          when     : int   — timestep at which emission begins
          duration : int   — number of timesteps the emission is active
          center   : tuple — (x, y) emission origin in [0,1]^2,
                             x: 0=left, 1=right; y: 0=bottom, 1=top
          radius   : float — emission region radius as fraction of domain [0,1]
          density  : float — smoke density injection rate
          fuel     : float — fuel injection rate
          heat     : float — temperature injection rate
          impulse  : tuple — (u, v) velocity impulse at emission point

    Returns
    -------
    dens_frames : np.ndarray, shape (steps, N, N), dtype float32
        Smoke density flux field rho(x, y, t) at each timestep.
        Values are non-negative; density scale depends on emission parameters.
        This array is saved as smoke_flux.npy by save_flux().
    """
    if config is None:
        config = DEFAULT_CONFIG
    if emissions is None:
        emissions = []

    # Field allocation
    u,  v   = alloc(N), alloc(N)
    u0, v0  = alloc(N), alloc(N)
    dens,  dens0  = alloc(N), alloc(N)
    fuel,  fuel0  = alloc(N), alloc(N)
    temp,  temp0  = alloc(N), alloc(N)
    noise, noise0 = alloc(N), alloc(N)
    p,     div    = alloc(N), alloc(N)

    # Grid coordinate arrays (used for circular emission masks)
    yy, xx = np.mgrid[0:N+2, 0:N+2]

    dens_frames = np.empty((steps, N, N), dtype=np.float32)

    for t in range(steps):
        u0.fill(0.0);  v0.fill(0.0)
        dens0.fill(0.0); fuel0.fill(0.0); temp0.fill(0.0)
        noise0.fill(0.0)

        # --- Apply emission events ---
        for e in emissions:
            start = int(e.get("when", 0))
            dur   = int(e.get("duration", 1))
            if not (start <= t < start + dur):
                continue

            cx, cy    = e.get("center", (0.5, 0.1))
            radius    = float(e.get("radius", 0.1))
            dens_add  = float(e.get("density", 1.0))
            fuel_add  = float(e.get("fuel",    dens_add))
            heat_add  = float(e.get("heat",    dens_add))
            u_imp, v_imp = e.get("impulse", (0.0, 0.0))

            # Map [0,1] domain coordinates to grid indices
            # x: 0=left → j=1, 1=right → j=N
            # y: 0=bottom → i=N (flipped), 1=top → i=1
            gx = int(np.clip(1 + cx * (N - 1), 1, N))
            gy = int(np.clip(1 + (1.0 - cy) * (N - 1), 1, N))
            r  = max(1, int(radius * (N - 1)))

            mask = (xx - gx)**2 + (yy - gy)**2 <= r * r
            dens0[mask] += dens_add
            fuel0[mask] += fuel_add
            temp0[mask] += heat_add
            u0[mask]    += u_imp
            v0[mask]    += v_imp

        # --- Fluid state update ---
        u    += dt * u0
        v    += dt * v0
        dens += dt * dens0
        fuel += dt * fuel0
        temp += dt * temp0

        combustion_step(
            temp, fuel, noise, dt=dt,
            burn_temperature  = config["BURN_TEMPERATURE"],
            cooling           = config["COOLING"],
            noise_blending    = config["NOISE_BLENDING"],
            fuel_dissipation  = config["FUEL_DISSIPATION"],
            N                 = N,
        )

        # Velocity advection and dissipation
        u0[:, :] = u;  v0[:, :] = v
        advect(1, u, u0, u0, v0, dt, N)
        advect(2, v, v0, u0, v0, dt, N)
        u[1:N+1, 1:N+1] *= config["VELOCITY_DISSIPATION"]
        v[1:N+1, 1:N+1] *= config["VELOCITY_DISSIPATION"]
        set_bnd(1, u, N)
        set_bnd(2, v, N)

        vorticity_confinement(u, v, conf=config["CONFINEMENT"], dt=dt, N=N)
        add_buoyancy(v, temp, buoyancy=config["BUOYANCY"], dt=dt, N=N)
        project(u, v, p, div, N=N, iters=20)

        # Scalar field advection and dissipation
        dens0[:, :] = dens
        advect(0, dens, dens0, u, v, dt, N)
        dens[1:N+1, 1:N+1] *= config["DENSITY_DISSIPATION"]

        temp0[:, :] = temp
        advect(0, temp, temp0, u, v, dt, N)

        fuel0[:, :] = fuel
        advect(0, fuel, fuel0, u, v, dt, N)
        fuel[1:N+1, 1:N+1] *= config["FUEL_DISSIPATION"]

        noise0[:, :] = noise
        advect(0, noise, noise0, u, v, dt, N)
        update_noise(noise, dt, config["NOISE_VOLATILITY"], N)

        np.clip(dens,  0.0, None, out=dens)
        np.clip(temp,  0.0, 1.0,  out=temp)
        np.clip(fuel,  0.0, 1.0,  out=fuel)
        np.clip(noise, 0.0, 1.0,  out=noise)

        dens_frames[t] = dens[1:N+1, 1:N+1]

    return dens_frames


# =============================================================================
# 11. Output
# =============================================================================

def save_flux(dens_frames: np.ndarray,
              output_path: str = "smoke_flux",
              fps: int = 24,
              gain: float = 3.0,
              gamma: float = 0.9) -> None:
    """
    Save the smoke flux field as a NumPy binary array and an optional GIF
    for visual inspection.

    Parameters
    ----------
    dens_frames : np.ndarray, shape (steps, N, N)
        Smoke density flux array produced by simulate().
    output_path : str
        Output file path without extension. Produces:
          {output_path}.npy  — binary array consumed by apply_smoke_degradation.py
          {output_path}.gif  — visual preview (requires imageio)
    fps   : int   — GIF frame rate (default: 24)
    gain  : float — contrast gain for GIF visualization (default: 3.0)
    gamma : float — gamma correction for GIF visualization (default: 0.9)
    """
    np.save(output_path + ".npy", dens_frames)
    print(f"Smoke flux saved to: {output_path}.npy  "
          f"(shape: {dens_frames.shape}, dtype: {dens_frames.dtype})")

    try:
        import imageio.v2 as imageio
        f   = np.nan_to_num(dens_frames.astype(np.float32))
        imgs = []
        for k in range(f.shape[0]):
            fk  = f[k]
            mx  = float(np.percentile(fk, 99.0))
            mx  = mx if (np.isfinite(mx) and mx > 1e-6) else 1.0
            g   = np.clip(fk / mx * gain, 0.0, 1.0)
            if gamma > 0:
                g = g ** (1.0 / gamma)
            imgs.append((g * 255.0 + 0.5).astype(np.uint8))
        imageio.mimsave(output_path + ".gif", imgs,
                        duration=1.0 / float(fps))
        print(f"Visual preview saved to: {output_path}.gif")
    except ImportError:
        print("imageio not found — GIF preview skipped. "
              "Install with: pip install imageio")


